    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {

        ArrayList<Contact_Data> Data = new ArrayList<>();
        jTextArea1.setText(" ");
        try{
            FileReader f = new FileReader("Files/Contacts_data.txt");
            BufferedReader b = new BufferedReader(f);
            String st;
            jTextArea1.append("     "+"Name"+"                                     "+"NID S.N"+"                           "+"Phone Number"+"                           "+"Email"+"                                            "+"Address");
            jTextArea1.append("\n");
            jTextArea1.append("\n");
            while (null != (st = b.readLine()))
            {

                String val[]=st.split("#");


                Contact_Data obj = new Contact_Data(val[0],Long.parseLong(val[1]),val[2],Long.parseLong(val[3]),val[4]);
                Data.add(obj);


            }
            Data.sort(new Comparator<Contact_Data>() {
                @Override
                public int compare(Contact_Data o1, Contact_Data o2) {
                    String n = o1.name;
                    String n2 = o2.name;
                    return n.compareTo(n2);
                }
            });
            for (Contact_Data s : Data){
              jTextArea1.append(s.name+"                  "+s.number+"                         "+s.phone+"              "+s.email+"              "+s.address);
                jTextArea1.append("\n");

            }



            b.close();



        }

        catch(IOException e){
            e.printStackTrace();
        }
    }


