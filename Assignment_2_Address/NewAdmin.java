    public void writeToFile(String username,String email, String password) throws IOException {

        File file=new File("Files/Admin_data.txt");
        FileWriter fw=new FileWriter(file,true);
        PrintWriter pw=new PrintWriter(fw);
        pw.write(username+"#"+email+"#"+password);
        pw.println();
        pw.close();


    }

